package classwork.Calculyator;

public class Calculyator {

    double plus(double a, double b ) {
        return a + b;
    }

    double minus(double a, double b) {
        return a - b;
    }

    double multiple(double a, double b) {
        return a * b;
    }

    double divine(double a, double b) {
        return a / b;

    }

}
